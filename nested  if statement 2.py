a=int(input("enter first number:"))
b=int(input("enter second number:"))
ch=input("enter operator")
if ch=="+":
    print("result =" ,a+b)
elif (ch=="-"):
    print("result =",a-b)
elif (ch=="*"):
    print("result =",a*b)
elif (ch=="/"):
    print("result =",a/b)
elif (ch=="%"):
    print("result=",a%b)
elif (ch=="//"):
    print("result =",a//b)
elif(ch=="**"):
    print("result =",a**b)
      
